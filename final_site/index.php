            <p>LONG COURSE SWIM (1.2mi): Participants will make two counter-clockwise loops. Large buoys will mark the turn points. Kayakers will be positioned on the water to support the swimmers. Medical support will be present on the beach.</p><br>

            <p>LONG COURSE BIKE (58 Miles): A scenic point-to-point course that travels over gently rolling hills prior to three hard climbs. The bike course will be marked with large directional signage and there will be course marshals at key intersections to help direct you.</p><br>

            <p>LONG COURSE RUN (13.1mi): A mostly flat loop course on widely paved bike paths that traverse through and around this beautiful and scenic destination resort (two hills total with a minimal elevation gain).</p><br>

            <p>OLYMPIC SWIM (1,500 meters): Participants will make two counter-clockwise loops. Large buoys will mark the turn points. Kayakers will be positioned on the water to support the swimmers. Medical support will be present on the beach.</p><br>

            <p>OLYMPIC BIKE (28mi): A scenic point-to-point course that travels over gently rolling hills. The bike course will be marked with large directional signage and course marshals will be present at key intersections.</p><br>

            <p>OLYMPIC RUN (10K): A mostly flat loop course on widely paved bike paths that traverse through and around this beautiful and scenic destination resort (one hill total with a minimal elevation gain).</p><br>
            <!--</div>-->
            <!--<div class="col span_6_of_12">-->
            <p>SPRINT: Course will offer a 1-loop of a half-mile swim, the exact same 28 mile Bike Course as the Olympic distance, and a 5km run.</p><br>

            <p>TRY-A-TRI: This novice race is designed for the first time triathlete, those new to the sport, and our Junior Triathletes. The swim is a shorter, more manageable quarter-mile distance, (instead of the standard half-mile Sprint distance swim), 10 mile bike ride (vs 12 miles and it's a two-loop course making it very spectator friendly!) and a flat two mile run (vs three mile sprint course).</p><br>

            <p>HALF-MARATHON COURSE: Half-Marathon (13.1-miles): Half-Marathon event starts and finishes in the Athletes Village to the cheers of the enthusiastic crowd. Once finished, runners can enjoy the finish line festivities, including the Sports &amp; Fitness Expo and live entertainment. Post-race refreshments will be provided and the Awards Ceremony for the Half-Marathon will begin once the results have been certified.</p><br>

            <p>10K COURSE: The 10K event starts and finishes in the Athletes Village. The paths are approximately six ft wide, perfectly paved and wind around through the forest. Each course has only two small hills with a minimal elevation gain to navigate and a fast downhill to flat finish to the roaring cheers of the crowd.</p><br>

            <p>SPLASH N' DASH: Kids event for participants ages 4 to 15. This is a 100 yard swim and a 50 yard dash.</p><br>
